package org.cyberelay.portletcontainer.legacy.descriptor.parser;

import java.io.FileNotFoundException;
import java.io.FileOutputStream;

import javax.xml.parsers.FactoryConfigurationError;
import javax.xml.parsers.ParserConfigurationException;
import javax.xml.transform.OutputKeys;
import javax.xml.transform.Result;
import javax.xml.transform.Source;
import javax.xml.transform.Transformer;
import javax.xml.transform.TransformerConfigurationException;
import javax.xml.transform.TransformerException;
import javax.xml.transform.TransformerFactory;
import javax.xml.transform.dom.DOMSource;
import javax.xml.transform.stream.StreamResult;

import org.w3c.dom.Document;
import org.w3c.dom.Element;

/**
 * @author Terry Li
 * 
 *
 */
public abstract class XmlWriter implements XmlConstants {

	public void write(String fileName) throws ParserConfigurationException,
			FactoryConfigurationError, FileNotFoundException {
		try {
			Source source = new DOMSource(buildDocument());
			Result result = new StreamResult(new FileOutputStream(fileName));
			Transformer xformer = TransformerFactory.newInstance().newTransformer();
			xformer.setOutputProperty(OutputKeys.INDENT, "yes");
			xformer.setOutputProperty("{http://xml.apache.org/xslt}indent-amount", "2");

			xformer.transform(source, result);
		} catch (TransformerConfigurationException e) {
			e.printStackTrace();
		} catch (TransformerException e) {
			e.printStackTrace();
		}
	}

	protected abstract Document buildDocument() throws ParserConfigurationException;

	protected void setElementText(Element element, String text) {
		element.appendChild(element.getOwnerDocument().createTextNode(text));
	}

}
